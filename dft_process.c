bool dft_process(int32_t *data,uint32_t time_len,int32_t *real,int32_t *img)
{
    uint16_t i,j;
    float w = 0.0f;
    int32_t temp_real,temp_img;
    /*
     *  sample rate: 4HZ                2*PI
     *  resolution : 0.01HZ             0.01*PI/2
     * */    
    w = 0.01*PI/2;
    
    for(i = 0;i < 37;i++)
    {
        temp_real   = 0;
        temp_img    = 0;
        for(j = 0;j < time_len;j++)
        {
            temp_real   = temp_real + data[j]*(int16_t)(255.0f*cos(i*j*w));
            temp_img    = temp_img  + data[j]*(int16_t)(255.0f*sin(i*j*w));
        }
        
        DEBUG_PRINT("%d\t%d\r\n",temp_real,temp_img);
    }
    
 

}
